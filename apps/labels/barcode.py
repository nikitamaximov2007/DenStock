"""Слой 23 — генератор линейного штрихкода Code128 (subset B) в inline-SVG.

Чистый, локальный, детерминированный: без внешних зависимостей, без сетевых
вызовов, без записи файлов. Кодирует только печатный ASCII (32..126) — этого
достаточно для системных кодов проекта (`ITEM:DS-…`, `LOC:…`, `DS-…`, заводские
ШК). Под штрихкодом этикетка всегда печатает человекочитаемый код (см. шаблоны),
поэтому при необходимости отката к текстовой этикетке смысл не теряется.

Структура символа Code128 — 11 модулей (стоп — 13: добавочный штрих). Контрольная
сумма — взвешенная по модулю 103: `(СТАРТ + Σ значение_i · i) % 103`, где i — позиция
символа данных, начиная с 1. См. тесты `tests/test_labels.py`.
"""
from xml.sax.saxutils import escape

_START_B = 104  # стартовый символ subset B
_STOP = 106  # стоп-символ (паттерн из 13 модулей)

# Канонические паттерны Code128 (индекс = значение символа 0..106). Каждая строка —
# ширины модулей, чередующиеся «штрих, пробел, штрих, …», начиная со штриха.
_PATTERNS = [
    "212222", "222122", "222221", "121223", "121322", "131222", "122213", "122312",
    "132212", "221213", "221312", "231212", "112232", "122132", "122231", "113222",
    "123122", "123221", "223211", "221132", "221231", "213212", "223112", "312131",
    "311222", "321122", "321221", "312212", "322112", "322211", "212123", "212321",
    "232121", "111323", "131123", "131321", "112313", "132113", "132311", "211313",
    "231113", "231311", "112133", "112331", "132131", "113123", "113321", "133121",
    "313121", "211331", "231131", "213113", "213311", "213131", "311123", "311321",
    "331121", "312113", "312311", "332111", "314111", "221411", "431111", "111224",
    "111422", "121124", "121421", "141122", "141221", "112214", "112412", "122114",
    "122411", "142112", "142211", "241211", "221114", "413111", "241112", "134111",
    "111242", "121142", "121241", "114212", "124112", "124211", "411212", "421112",
    "421211", "212141", "214121", "412121", "111143", "111341", "131141", "114113",
    "114311", "411113", "411311", "113141", "114131", "311141", "411131", "211412",
    "211214", "211232", "2331112",
]


def encode_code128_b(value: str) -> list[int]:
    """Строка → список кодовых значений Code128 B: `[СТАРТ, …данные, контрольная, СТОП]`.

    Поддерживает только печатный ASCII (32..126). Это покрывает все системные коды
    проекта; иной символ — ошибка (этикетка деградирует до текста, см. `code128_svg`).
    """
    if not value:
        raise ValueError("Пустая строка не кодируется в штрихкод.")
    codes = [_START_B]
    for ch in value:
        point = ord(ch)
        if point < 32 or point > 126:
            raise ValueError(f"Символ {ch!r} вне диапазона Code128 B (32..126).")
        codes.append(point - 32)
    checksum = _START_B
    for position, code in enumerate(codes[1:], start=1):
        checksum += code * position
    codes.append(checksum % 103)
    codes.append(_STOP)
    return codes


def code128_svg(value: str, *, module: int = 2, height: int = 56, quiet: int = 10) -> str:
    """Вернуть строку inline-SVG со штрихкодом Code128 B для `value`.

    `module` — ширина одного модуля в px, `height` — высота штрихов, `quiet` —
    тихая зона по краям (в модулях). Детерминированно; ничего не пишет на диск.
    """
    codes = encode_code128_b(value)
    bars: list[tuple[int, int]] = []  # (x в модулях, ширина в модулях)
    x = quiet
    for code in codes:
        is_bar = True
        for digit in _PATTERNS[code]:
            width = int(digit)
            if is_bar:
                bars.append((x, width))
            x += width
            is_bar = not is_bar
    total_modules = x + quiet
    svg_width = total_modules * module
    rects = "".join(
        f'<rect x="{bx * module}" y="0" width="{bw * module}" height="{height}"/>'
        for bx, bw in bars
    )
    return (
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{svg_width}" height="{height}" '
        f'viewBox="0 0 {svg_width} {height}" role="img" aria-label="{escape(value)}">'
        f'<rect x="0" y="0" width="{svg_width}" height="{height}" fill="#fff"/>'
        f'<g fill="#000">{rects}</g></svg>'
    )


def safe_code128_svg(value: str) -> str:
    """Как `code128_svg`, но при невозможности кодирования возвращает "" (деградация).

    Используется во вьюхах: непригодный код просто не получает графики — человекочитаемый
    текст всё равно печатается шаблоном, страница не падает.
    """
    if not value:
        return ""
    try:
        return code128_svg(value)
    except ValueError:
        return ""
